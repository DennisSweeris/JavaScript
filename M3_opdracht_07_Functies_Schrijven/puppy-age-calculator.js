let calculateDogAge = function (puppyAge) {
    let dogYears = 7 * puppyAge;
    console.log("Your puppy is " + dogYears + " years old in dog years!");
}

calculateDogAge(3);
calculateDogAge(6.3);
calculateDogAge(12);

